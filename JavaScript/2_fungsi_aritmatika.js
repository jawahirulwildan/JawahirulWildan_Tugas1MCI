function penjumlahan(a, b) {
    return a + b;            
}

function pengurangan(a, b) {
    return a - b;            
}

function perkalian(a, b) {
    return a * b;            
}

function pembagian(a, b) {
    return a / b;            
}

export{ penjumlahan, pengurangan, perkalian, pembagian };